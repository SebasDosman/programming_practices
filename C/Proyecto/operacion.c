#include "operacion.h"

int suma(int a, int b){
  int resultado = (a+b);
  return resultado;
}

int resta(int a, int b){
  int resultado = (a-b);
  return resultado;
}

int multiplicacion(int a, int b){
  int resultado = (a*b);
  return resultado;
}

int division(int a, int b){
  int resultado = (a/b);
  return resultado;
}