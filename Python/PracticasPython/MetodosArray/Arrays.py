arreglo = ["Ruby", "Perl", "C", "Go", "Rust"]
print(arreglo)

print("\n Append")
arreglo.append("Python")
print(arreglo)

print("\n Insert")
arreglo.insert(0, "C++")
print(arreglo)

print("\n Count")
arreglo.count("Perl")
print(arreglo)

print("\n Remove")
arreglo.remove("Rust")
print(arreglo)

print("\n Sort")
arreglo.sort()
print(arreglo)

print("\n Reverse")
arreglo.reverse()
print(arreglo)